// ✅ 단골손님 버튼 클릭 시 모달 자동 생성
$(document).on("click", ".regularGuestsBtn", function () {
    // 이미 모달이 존재하면 다시 안 만듦
    if ($("#regularGuestsModal").length === 0) createRegularGuestsModal();
    $("#regularGuestsModal").fadeIn(200);
    renderGuestList();
});

let currentGuest = null;

// ✅ 모달 생성 함수
function createRegularGuestsModal() {
    const modalHTML = `
    <div id="regularGuestsModal" class="login-overlay" style="display:none;">
      <div class="login-modal" style="position:relative;">
        <button id="closeRegularGuestsModal" class="closeBtn" style="color: ${COLORS.P} !important;">✕</button>
        <h2 class="modal-title">단골손님 목록</h2>
        <div id="guestList"></div>
        <div id="levelSelect" style="display:none; margin-top:15px;">
          <h3 class="sub-title">레벨 선택</h3>
          <button class="levelBtn" data-level="Lv1_2">Lv. 1~2</button>
          <button class="levelBtn" data-level="Lv3_4">Lv. 3~4</button>
          <button class="levelBtn" data-level="Lv5">Lv. 5</button>
        </div>
        <div id="dialogueList" style="display:none; margin-top:15px;">
          <h3 class="sub-title">대사 목록</h3>
          <ul id="dialogueItems" style="list-style:none; padding:0; line-height:1.8; text-align:left;"></ul>
        </div>
        <button id="guestBackBtn" style="display:none; position:absolute; top:8px; left:10px; height: 43px; width: 40px; background: none; color: ${COLORS.P}">←</button>
      </div>
    </div>
  `;
    $("body").append(modalHTML);

    // 닫기
    $("#closeRegularGuestsModal").on("click", function () {
        $("#regularGuestsModal").fadeOut(200);
    });

    // 뒤로가기
    $("#guestBackBtn").on("click", function () {
        if ($("#dialogueList").is(":visible")) {
            $("#dialogueList").hide();
            $("#levelSelect").fadeIn(150);
            $(".modal-title").text(currentGuest);
        } else if ($("#levelSelect").is(":visible")) {
            $("#levelSelect").hide();
            $("#guestList").fadeIn(150);
            $("#guestBackBtn").hide();
            $(".modal-title").text("단골손님 목록");
            currentGuest = null;
        }
    });
}

// ✅ 1단계: 손님 목록 (그리드 + 개별 프로필 이미지)
function renderGuestList() {
    const $list = $("#guestList");
    $list.empty().show();
    $("#levelSelect, #dialogueList, #guestBackBtn").hide();
    $(".modal-title").text("단골손님 목록");

    // 🔹 손님 목록을 그리드로 표시
    $list.css({
        display: "grid",
        gridTemplateColumns: "repeat(auto-fill, minmax(90px, 1fr))",
        gap: "10px",
        justifyItems: "center"
    });

    // 🔹 손님 이름 배열
    const guests = Object.keys(regularGuests);

    guests.forEach((guest, index) => {
        const imgSrc = `../image/profile${index + 1}.jpg`;

        const $btn = $(`
            <div class="guestCard">
                <img src="${imgSrc}" alt="${guest}" class="guestImg" />
                <div class="guestName">${guest}</div>
            </div>
        `);

        // 🔹 카드 스타일
        $btn.css({
            width: "90px",
            textAlign: "center",
            cursor: "pointer",
            border: `1px solid ${COLORS.BO_S}`,
            borderRadius: "10px",
            paddingTop: "8px",
            background: `${COLORS.BG_S}`,
            transition: "0.2s",
        });

        $btn.find(".guestImg").css({
            width: "60px",
            height: "60px",
            borderRadius: "8px",
            objectFit: "cover",
            display: "block",
            margin: "0 auto 6px"
        });

        $btn.find(".guestName").css({
            fontFamily: "Pretendard",
            fontSize: "14px",
            color: `${COLORS.TXT_M}`
        });

        // 🔹 클릭 시 레벨 선택 이동
        $btn.on("click", function () {
            showLevelSelect(guest);
        });

        $list.append($btn);
    });
}

// ✅ 2단계: 레벨 선택
function showLevelSelect(guestName) {
    currentGuest = guestName;
    $("#guestList").hide();
    $("#levelSelect").fadeIn(150);
    $("#guestBackBtn").show();
    $(".modal-title").text(`${guestName}`);

    $(".levelBtn").off("click").on("click", function () {
        const level = $(this).data("level");
        showDialogues(guestName, level);
    });
}

// ✅ 3단계: 대사 표시
function showDialogues(guestName, level) {
    const dialogues = regularGuests[guestName][level];
    const $ul = $("#dialogueItems");
    $ul.empty();

    if (!dialogues || dialogues.length === 0) {
        $ul.append("<li>등록된 대사가 없습니다.</li>");
    } else {
        dialogues.forEach(line => {
            $ul.append(`<li>• ${line}</li>`);
        });
    }

    $("#levelSelect").hide();
    $("#dialogueList").fadeIn(150);
    $(".modal-title").text(`${guestName} (${level.replace("_", "~")})`);
}